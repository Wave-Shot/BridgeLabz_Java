// Creating class AgeCalculation to calculate age
class AgeCalculation {

    public static void main(String[] args) {

        // Variable to store name
        String name = "Harry";

        // Variable to store birth year
        int birthYear = 2000;

        // Variable to store current year
        int currentYear = 2024;

        // Calculating age
        int age = currentYear - birthYear;

        // Displaying result
        System.out.println(name + "'s age in " + currentYear + " is " + age);
    }
}
